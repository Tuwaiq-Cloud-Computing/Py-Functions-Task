list8 = [1, 0, 3, 4, 0, 0]

def rearrange_zeros(lst):
    zeros = [x for x in lst if x == 0]
    non_zeros = [x for x in lst if x != 0]
    return non_zeros + zeros
print("-----------------------------------Question8---------------------------------------")
print(rearrange_zeros(list8))
print("-----------------------------------------------------------------------------------")